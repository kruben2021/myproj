package za.ac.cput;

public class Calc {
    /**
     * Adds two integers
     * @param a
     * @param b
     * @return int
     */
    public static int add(int a, int b) {
        return (a + b);
    }

    /**
     * Adds two real numbers
     * @param a
     * @param b
     * @return double
     */
    public static double add(double a, double b) {
        return (a + b);
    }

    /**
     *
     * @param a
     * @param b
     * @return String
     */
    public static String add(String a, String b) {
        return (a + b);
    }
}
